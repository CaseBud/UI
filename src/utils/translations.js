// Function to get translated text
export const translate = (key, language = 'en-US') => {
    if (!translations[key]) {
        console.warn(`Translation key not found: ${key}`);
        return key;
    }
    
    if (!translations[key][language]) {
        console.warn(`Translation not available for language ${language}, key: ${key}`);
        return translations[key]['en-US'] || key;
    }
    
    return translations[key][language];
};

// Component for easy usage in JSX
export const T = ({ id, defaultMessage }) => {
    return translate(id, localStorage.getItem('userLanguage') || 'en-US') || defaultMessage || id;
};